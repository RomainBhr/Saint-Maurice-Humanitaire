<br><p id=orange><u><i>Qui sommes-nous ?</p></i></u>
L’association Saint Maurice Humanitaire travaille et œuvre depuis quelque temps avec la<br> 
volonté, mais aussi l'envie d’apporter un peu d’humanité à ce pays qu'est le Sénégal.<br>
Les carences alimentaires, l'absence d'école, la pauvreté, le manque d'eau entrainent le désarroi des villageois.<br> 
Certains villageois ont besoin de faire des kms pour trouver de l’eau.<br>
Nos axes d'actions sont l’apport en eau, la scolarisation, l'apport de fournitures scolaires, vestimentaires, médicales et alimentaires.<br> 
Ces actions se déroulent dans la région de Siné Saloum, dans des villages de brousse.<br>

